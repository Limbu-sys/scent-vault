let currentLang = localStorage.getItem("sv_lang") || "ru";

export function getLang() {
  return currentLang;
}

export function t(key) {
  return STRINGS[currentLang]?.[key] ?? STRINGS.ru[key] ?? key;
}

export function setLang(lang) {
  if (!STRINGS[lang]) return;
  currentLang = lang;
  localStorage.setItem("sv_lang", lang);
  document.documentElement.lang = lang;
  applyI18n();
  document.dispatchEvent(new CustomEvent("langchange", { detail: lang }));
}

export function applyI18n() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    const text = t(key);
    if (text.includes("\n")) {
      el.innerHTML = text.replace(/\n/g, "<br>");
    } else {
      el.textContent = text;
    }
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll(".lang-pill__btn").forEach((btn) => {
    btn.classList.toggle("lang-pill__btn--active", btn.dataset.lang === currentLang);
  });
}

export function formatPrice(amount) {
  return new Intl.NumberFormat(currentLang === "ru" ? "ru-RU" : "en-US", {
    style: "currency",
    currency: "RUB",
    maximumFractionDigits: 0,
  }).format(amount);
}

export function methodName(method, lang = currentLang) {
  const key = lang === "en" ? "name_en" : "name_ru";
  return method[key] || method.name_ru || method.id;
}
